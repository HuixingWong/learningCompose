@Composable
fun ScrollBoxes() {
    Column(
        modifier = Modifier
            .background(Color.LightGray)
            .size(100.dp)
            .verticalScroll(rememberScrollState())
    ) {
        repeat(10) {
            Text(
                "Item $it",
                modifier = Modifier.padding(2.dp)
            )
        }
    }
}

